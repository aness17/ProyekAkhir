                                <p>
                                    PEKANBARU (RIAUPOS.CO) - Penghargaan bergengsi kembali diraih Kota Pekanbaru dalam skala nasional. Kali ini, melalui Kue Talam Durian pada Anugerah Pesona Indonesia.
                                    Kue ini jadi juara 2 makanan tradisional terpopuler di Indonesia.


                                </p>

                                <p>
                                    Pemberian penghargaan ini dilakukan, Jumat (22/11) di Balairung Soesilo Sudarman, Kementerian Pariwisata Ekonomi Kreatif RI,
                                    Jakarta Pusat. Hadir mewakili Wali Kota (Wako) Pekanbaru Dr H Firdaus ST MT menerima penghargaan Asisten II Bidang
                                    Pembangunan Sekretariat Daerah Kota (Setdako) Pekanbaru El Syabrina.
                                </p>
                                <p>
                                    El Syabrina menerima penghargaan diberikan langsung oleh Staf Ahli Kemenpar bidang Multikultural Guntur
                                    Sakti.''Anugerah Pesona Indonesia (API) merupakan rangkaian kegiatan tahunan yang diselenggarakan
                                    Kemenpar dalam upaya membangkitkan apresiasi masyarakat terhadap Pariwisata Indonesia.
                                    Alhamdulillah kita mendapatkan penghargaan makanan tradisional terpopuler lewat Kue Talam Durian,'' kata dia.
                                    Anugerah Pesona Indonesia selain bertujuan mengembangkan pariwisata Indonesia, juga bertujuan untuk mendorong
                                    peran serta berbagai pihak terutama pemerintah daerah untuk lebih berupaya dalam mempromosikan pariwisata di
                                    daerahnya masing-masing.
                                </p>
                                <img src="<?= base_url('assets/') ?>pelanggan/assets/img/ketan talam durian.png" alt="" class="img-fluid" width="400px">

                                <p>
                                    Kue talam durian yang berbahan dasar ketan dan durian itu dipelopori oleh warga yang sebelumnya sukses membangun bisnis rumahan " Kue Viera". Salah satu kue favorit dan andalan di usaha rumahan tersebut adalah kue talam durian. </p>
                                <p>
                                    Kepala Dinas Kebudayaan dan Pariwisata (Disbudpar) Kota Pekanbaru Nurfaisal di Pekanbaru Sebelumnya memberikan
                                    apresiasi atas capaian dari kue talam durian yang kala itu masuk nominasi.''Kita berharap oleh-oleh tradisional khas
                                    Kota Pekanbaru ini bisa meraih prestasi terbaik di API 2019,'' katanya.
                                </p>
                                <p>Pemilihan pemenang dalam anugerah ini dilakukan berdasarkan polling masyarakat dengan mengirimkan SMS dukungan.
                                    Polling Sebelumnya berlangsung dari 1 Juni 2019 hingga 31 Oktober 2019. Kategori penghargaan API antara lain wisata
                                    halal terpopuler, wisata air terpopuler, obyek wisata baru terpopuler, atraksi budaya terpopuler dan sejumlah penghargaan
                                    bergengsi lainnya.(ADV)
