                        <h2 class="title">Viera Oleh-oleh Bekerjasama dengan Dinas Pariwisata dan Kebudayaan Kota Pekanbaru</h2>

                        <div class="meta-top">
                            <ul>
                                <li class="d-flex align-items-center"><i class="bi bi-person"></i> <a href="https://www.pekanbaru.go.id/p/news/wawako-resmikan-nama-baru-rumah-kue-viera-jadi-viera-oleh-oleh">Berita Pemko</a></li>
                                <li class="d-flex align-items-center"><i class="bi bi-clock"></i> <a href="blog-details.html"><time datetime="2020-01-01">Des 18, 2021</time></a></li>
                            </ul>
                        </div><!-- End meta top -->

                        <div class="content">
                            <p>
                                Wakil Walikota (Wawako) Pekanbaru H. Ayat Cahyadi, meresmikan perubahan nama atau rebranding Rumah Kue Viera menjadi Viera Oleh-oleh, Sabtu (18/12).
                            </p>
                            <p>
                                Pada kegiatan yang dipusatkan di toko utama Viera Oleh-oleh di Jalan Melati ini, turut juga dilakukan Grand Opening satu unit bisnis baru yang dinamakan Viera Souvenir.
                            </p>

                            <p>
                                "Kami atas nama Pemerintah Kota Pekanbaru menyambut baik serta mengucapkan selamat atas grand opening bisnis baru Viera Oleh-oleh sebagai suatu bentuk peran dunia usaha dalam pemberdayaan ekonomi
                                kerakyatan dan meningkatkan perekonomian daerah serta kesejahteraan masyarakat Kota Pekanbaru," ucap Wawako dalam sambutannya.</p>

                            <p>
                                Menurutnya, kehadiran gerai Viera Oleh-oleh merupakan salah satu indikator kemajuan Kota Pekanbaru yang terus berkembang pesat dan telah berkembang menjadi kota dunia usaha.</p>

                            <p>
                                "Kami juga berbahagia, karena selain menjual produk buatan sendiri, Viera Oleh-oleh juga memasarkan produk-produk UMKM tempatan. Hal ini menjadi nilai tersendiri bagi gerai Viera Oleh-oleh, karena mampu mengangkat usaha masyarakat setempat," ujarnya.</p>
                            <img src="<?= base_url('assets/') ?>pelanggan/assets/img/dinas.jpeg" alt="" class="img-fluid" width="400px">

                            <p>
                                Dikatakan wawako, saat ini pola pikir masyarakat terus meningkat. Dari dahulunya hanya berada dalam zona nyaman, sekarang sudah berkembang dengan inovasi dan kreativitas. Salah satunya adalah dengan membuka peluang usaha untuk menciptakan lapangan pekerjaan yang berfikir dan bekerja dengan kreativitas yang tinggi, inovasi, serta semangat dan jiwa entrepreneur.
                            </p>
                            <p>
                                "Kepada owner Viera Oleh-oleh, saya mengucapkan selamat dan tahniah. Semoga Grand Launching dan Rebranding ini dapat berjaya dan sukses mendapatkan tempat di hati masyarakat Kota Pekanbaru," harapnya.
                            </p>
                            <p>
                                Sementara itu, wner Viera Oleh-oleh Rahmat mengatakan pihaknya selalu berorientasi untuk mengembangkan bisnis rumah kuenya itu, salah satunya dengan menjadi leading sector di bisnis oleh-oleh.
                            </p>
                            <p>
                                Setelah Rumah Kue Viera (RKV) berhasil menjadi pemimpin pasar untuk oleh-oleh dalam bentuk kue dan yang sejenis, RKV ingin melengkapi kebutuhan wisatawan dan masyarakat untuk produk souvenir khas Riau. </p>
                            <p>"Kami bertransformasi dari yang dulunya hanya rumah kue atau kuliner, sekarang melebarkan sayap sebagai pusat oleh-oleh khas Riau. Untuk itu, RKV membentuk satu unit bisnis baru yang dinamakan Viera Souvenir dan rebranding menjadi Viera Oleh-oleh," kata Rahmat didampingi istrinya, Evi.
                            </p>
                            <p>
                                Viera Souvenir, kata Rahmat menghadirkan berbagai souvenir khas Riau seperti batik, tenun, songket, tanjak, kaos merchandise, gantungan kunci, topi dan banyak lagi.
                            </p>
                            <p>
                                "Produk yang tersedia di outlet Viera Souvenir diperoleh dari berbagai UMKM seluruh Riau. Dan kerjasama ini juga bermaksud untuk meningkatkan akses pasar bagi para pelaku UMKM Riau," sebutnya.</p>
                            <p>Ia menegaskan bahwa kerjasama yang dibangun dengan para UMKM yang menjadi mitra Viera Souvenir ini tidak hanya dari sisi penjualan tapi juga pengembangan bisnis.
                                "Sehingga Viera dan UMKM Riau bisa tumbuh dan berkembang bersama," ungkapnya.
                            </p>
                            <p>
                                Untuk diketahui, RKV didirikan oleh Rahmad dan Evi pada 2015 lalu. Usaha rumahan ini dimulai dari rumah kecil di Perumahan Bidadari Panam. Awalnya mereka menjajakan dan berbagi kue yang mereka buat kepada warga disekitar tempat tinggalnya hingga berkembang seperti saat ini.
                            </p>
                            <p>
                                Rumah Kue Viera memang #berangkatdarirumah tapi tidak berhenti di rumah saja. Maka untuk terus tumbuh, mampu bersaing dan menjadi indentitas merk yang kuat Rumah Kue Viera siap berubah menjadi Viera Oleh-oleh.
                            </p>
                            <p>Nama Viera Oleh-oleh sekaligus menjadi penanda bahwa Viera siap mejadi pusat oleh-oleh terlengkap dan terbesar di Riau. Ditambah lagi dengan Tagline #Tandasayangdaririau, Viera ingin memberikan ungkapan kepada setiap pengunjung yang datang dan membawa pulang sebuah tanda sayang untuk orang tersayang yang sedang menunggu di rumah. (Kominfo2/RD1)</p>
